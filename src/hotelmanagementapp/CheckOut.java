    private void confirmbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmbuttonMouseClicked
        // TODO add your handling code here
         conn=DBconnection.GetConnection();
         try{
              String query="Insert into availableroom (RoomNumber,RoomType,RoomCost,BedType) value(?,?,?,?) ";
             pst=conn.prepareStatement(query ); 
             
              pst.setString(1, RoomNoField.getText());
               pst.setString(2,  RoomTypeField.getText());
                pst.setString(3, CostField1.getText());
                 pst.setString(4,   BedTypeField.getText());
              
              pst.execute();
              JOptionPane.showMessageDialog(this,"CONFIRMED..!");
         }
         catch(Exception e){
              JOptionPane.showMessageDialog(this,e);
         }
        try{
              String query="Insert into checkoutlist (Name,Email,ContactNo,NID,CheckIN,CheckOut) value(?,?,?,?,?,?) ";
             pst=conn.prepareStatement(query ); 
             
              pst.setString(1, NameField.getText());
               pst.setString(2,  EmailField1.getText());
                pst.setString(3, ContactField.getText());
                 pst.setString(4,   NidField1.getText());
               pst.setString(5,  CheckinField.getText());
                pst.setString(6,   CheckoutField.getText());
              pst.execute();
              JOptionPane.showMessageDialog(this,"CONFIRMED..!");
         }
         catch(Exception e){
              JOptionPane.showMessageDialog(this,e);
         }
        
        
        
        
        
        
        
        //code for cleaning
        clear();
      
    }//GEN-LAST:event_confirmbuttonMouseClicked

