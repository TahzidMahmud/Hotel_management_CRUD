    private void IdsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IdsearchKeyReleased
        // TODO add your handling code here:
         conn=DBconnection.GetConnection();
         try{
             
             String query="select * from customerinfo where CustomerId =? ";
              pst=conn.prepareStatement(query ); 
              
              pst.setString(1, Idsearch.getText());
              
              rs= pst.executeQuery();
         
               if(rs.next()){
                   Float add1=rs.getFloat("Payment");
                  String p = Float.toString(add1);
                
                  paymentfield.setText(p+""+"BDT");
                  String add2=rs.getString("Name");
                   NameField.setText(add2);
                   String add3=rs.getString("Address");
                    AddressField1.setText(add3);
                    String add4=rs.getString("Contact");
                    ContactField.setText(add4);
                     String add5=rs.getString("Email");
                     EmailField1.setText(add5);
                      String add6=rs.getString("Nationality");
                      NationalityField1.setText(add6);
                       String add7=rs.getString("Nid");
                       NidField1.setText(add7);
                       String add8=rs.getString("Gender");
                       GenderField.setText(add8);
                       String add9=rs.getString("Roomnumber");
                       RoomNoField.setText(add9);
                       String add10=rs.getString("RoomType");
                       RoomTypeField.setText(add10);
                       String add11=rs.getString("BedType");
                       BedTypeField.setText(add11);
    
     
                       String add12=rs.getString("Checkin");
                        CheckinField.setText(add12);
                       String add13=rs.getString("Checkout");
                       CheckoutField.setText(add13);
                       String add14=rs.getString("Roomcost");
                       CostField1.setText(add14);
                        String add15=rs.getString("CustomerId");
                         CustomeridField.setText(add15);
               }
               
               if(Idsearch.getText().equals("")){
                 clear();
                   
               }
               conn.close();
         }
         
         catch(Exception e){
              JOptionPane.showMessageDialog(null,e);
         }
        
        
        
    }//GEN-LAST:event_IdsearchKeyReleased

